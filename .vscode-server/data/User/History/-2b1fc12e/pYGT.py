# Create a calculator that takes two numbers and an operator (+, -, *, /, //, %, **) as input and performs the appropriate calculation.


num1=float(input("Please enter the first number: "))
num2=int(input("Please enter the second number: "))

print(num1+num2)
print(num1-num2)
print(num1*num2)
print(num1/num2)
print(num1//num2)
print(num1%num2)
print(num1**num2)


